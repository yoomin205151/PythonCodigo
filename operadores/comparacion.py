a = 5
b = 10

igual = a == b
no_igual = a != b
mayor_que = a > b
menor_que = a < b
mayor_o_igual_que = a >= b
menor_o_igual_que = a <= b

print("Igual:", igual)
print("No igual:", no_igual)
print("Mayor que:", mayor_que)
print("Menor que:", menor_que)
print("Mayor o igual que:", mayor_o_igual_que)
print("Menor o igual que:", menor_o_igual_que)
